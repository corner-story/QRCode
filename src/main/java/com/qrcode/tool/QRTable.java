    public static String getFormatStringBits(int ECCLevel, int maskPattern){
        assert ECCLevel <= 3 && ECCLevel >= 0;
        assert maskPattern <= 7 && maskPattern >= 0;
        return FormatStrings[ECCLevel * 8 + maskPattern];
    }

